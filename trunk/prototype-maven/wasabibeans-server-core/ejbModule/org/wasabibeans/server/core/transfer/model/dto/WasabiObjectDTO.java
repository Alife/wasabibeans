package org.wasabibeans.server.core.transfer.model.dto;

import java.io.Serializable;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlType;

@XmlAccessorType(XmlAccessType.FIELD)
@XmlType(name="WasabiObjectDTO", namespace="org.wasabibeans.server.core.transfer.model.dto")
public abstract class WasabiObjectDTO implements Serializable {

	@XmlElement(namespace="org.wasabibeans.server.core.transfer.model.dto",required=true)
	private static final long serialVersionUID = -3996987979960095763L;
	
	@XmlElement(namespace="org.wasabibeans.server.core.transfer.model.dto",required=true)
	protected String id;

	protected WasabiObjectDTO() {

	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	@Override
	public int hashCode() {
		return (id != null ? id.hashCode() : 0);
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (!(obj instanceof WasabiObjectDTO))
			return false;
		WasabiObjectDTO other = (WasabiObjectDTO) obj;
		if (id == null) {
			if (other.getId() != null)
				return false;
		} else if (!id.equals(other.getId()))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return getClass().getSimpleName() + "[id=" + id + "]";
	}

}
